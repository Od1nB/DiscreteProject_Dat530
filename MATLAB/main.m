dyn.ft = {'tBusDriverGenerator', 1,...
    'tCONNECT1', 1,...
    'tCONNECT2', 1,...
    'tCONNECT3', 1,...
    'tCONNECT4', 1,...
    'tCONNECT5', 1,...
    'tChangeDriver', 1,...
    'tCheckOut', 1,...
    'tDriveToBusStopBus', 1,...
    'tDriveToBusStopCar', 1,...
    'tGenerator', 1,...
    'tLobby', 1,...
    'tPick_up_bus_from_parking', 'normrnd(2,0.5)',...
    'tPick_up_car', 'normrnd(10,1)',...
    'tRoute2', 1,...
    'tRoute2_complete', 1,...
    'tRoute4', 1,...
    'tRoute4_complete', 1,...
    'tRoute6', 1,...
    'tRoute6_complete', 1,...
    'tRoutex60_postChange', 1,...
    'tRoutex60_postChange_complete', 1,...
    'tRoutex60_preChange', 1,...
    'tRoutex60_prechange_complete', 1,...
    'tTakeBusToParking', 1,...
    'tTakeCarToParking', 1,...
    'tWait', 1,...
    };

% initial state
